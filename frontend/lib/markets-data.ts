export const fmt = {
  price: (n: number) =>
    n >= 1000
      ? n.toLocaleString("en-US", { maximumFractionDigits: 2 })
      : n.toFixed(n < 1 ? 4 : 2),
  vol: (n: number) => {
    if (n >= 1e9) return `$${(n / 1e9).toFixed(2)}B`;
    if (n >= 1e6) return `$${(n / 1e6).toFixed(2)}M`;
    if (n >= 1e3) return `$${(n / 1e3).toFixed(2)}K`;
    return `$${n.toFixed(2)}`;
  },
  pct: (n: number) => `${n >= 0 ? "+" : ""}${n.toFixed(2)}%`,
};
